    private void preencherTabela(String campoPesquisa){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableAlunos.getModel();
        modelo.setNumRows(0);
        try{
            AlunoDao dao = new AlunoDao();
            List<Aluno> lista = dao.buscarPorID(campoPesquisa);
            
            for(Aluno obj : lista){
                ResponsaveisDao responsavelDao = new ResponsaveisDao();
                Responsavel r = responsavelDao.buscarPorId(obj.getIdresponsavel());
                
                String[] linha = {
                obj.getId().toString(),
                obj.getNome(),
                r.getNome(),
                
                ""
                };
                modelo.addRow(linha);
            }
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Pesquisar. " + ex.getMessage());
        }
    }
    
    
    
