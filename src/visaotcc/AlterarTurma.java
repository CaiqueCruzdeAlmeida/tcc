public void HabilitarCadastrar() {
    jButton1.setEnabled(false); // Começa desativado

    // Crie um DocumentListener para monitorar os campos de texto
    DocumentListener documentListener = new DocumentListener() {
        @Override
        public void insertUpdate(DocumentEvent e) {
            checkFields();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            checkFields();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            checkFields();
        }
    };

    // Adicione o DocumentListener aos campos relevantes
    
    jTextFieldQtd.getDocument().addDocumentListener(documentListener);
    jTextFieldValor.getDocument().addDocumentListener(documentListener);
    jTextFieldNome.getDocument().addDocumentListener(documentListener);
    

   
    checkFields();
}
public void checkFields() {
    String qtd = jTextFieldQtd.getText();
    String valor = jTextFieldValor.getText();
    String nome = jTextFieldNome.getText();

    // Habilitar o botão apenas se todos os campos estiverem preenchidos
    jButton1.setEnabled(!qtd.isEmpty() && !valor.isEmpty() && !nome.isEmpty());
}

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Integer idturma = Integer.parseInt(jTextFieldID.getText());
        String nometurma = jTextFieldNome.getText();
        String atividades = jTextAreaAtividades.getText();
        //String lotacaomaxima = jTextFieldLotacao.getText();
        String quantidadematriculados = jTextFieldQtd.getText();
        
        Double valormensalidade = Double.valueOf(jTextFieldValor.getText().replaceAll(",", "."));
        
        //validações
        
         if(idturma.equals("")){
            JOptionPane.showMessageDialog(this, "informe o ID.");
            jTextFieldID.requestFocus();
            return;
        }
          if(nometurma.equals("")){
            JOptionPane.showMessageDialog(this, "informe o Nome.");
            jTextFieldNome.requestFocus();
            return;
        }
          if(atividades.equals("")){
            JOptionPane.showMessageDialog(this, "informe as Atividades.");
            jTextAreaAtividades.requestFocus();
            return;
        }
          /*if(lotacaomaxima.equals("")){
            JOptionPane.showMessageDialog(this, "informe a Lotação Máxima da turma.");
            jTextFieldLotacao.requestFocus();
            return;
        }else if(!validarLotacao(lotacaomaxima)){
            return;
        }*/
          if(quantidadematriculados.equals("")){
            JOptionPane.showMessageDialog(this, "informe a Quantidade de Alunos Matriculados na turma.");
            jTextFieldQtd.requestFocus();
            return;
        }
        if(valormensalidade.equals("")){
            JOptionPane.showMessageDialog(this, "informe o Valor a pagar.");
            jTextFieldValor.requestFocus();
            return;
        }
          Turma u = new Turma(idturma, nometurma, quantidadematriculados, atividades, valormensalidade, null);
          try {
            TurmaDao dao = new TurmaDao();
            dao.atualizar(u);
            gerenciarForm.callback();

            JOptionPane.showMessageDialog(this, "Registro atualizado.");
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
          
    }//GEN-LAST:event_jButton1ActionPerformed

