    public void mostrarTurma(String idTexto){
       try{
           Integer idturma = Integer.parseInt(idTexto);
           
           TurmaDao dao = new TurmaDao();
           Turma obj = dao.getTurma(idturma);
           
           if (obj != null){
               //Preenche os dados do formulário
               jTextFieldID.setText(Integer.toString(obj.getIdTurma()));
               jTextFieldNome.setText(obj.getNometurma());
               jTextAreaAtividades.setText(obj.getAtividades());
               //jTextFieldLotacao.setText(obj.getLotacaomaxima());
               jTextFieldQtd.setText(obj.getQuantidadematriculados());
               jTextFieldValor.setText(obj.getValormensalidade().toString());
               
           }else{
               JOptionPane.showMessageDialog(this, "Registro não encontrado");               
               jTextFieldID.setText("");
               jTextFieldNome.setText("");
               //jTextFieldLotacao.setText("");
               jTextAreaAtividades.setText("");
               jTextFieldQtd.setText("");
               jTextFieldValor.setText("");
               jTextFieldNome.requestFocus();
           }
           
       }catch(Exception e){
             //JOptionPane.showMessageDialog(rootPane, e);
               JOptionPane.showMessageDialog(this, "Erro ao consultar registro");              
               jTextFieldID.setText("");   
               jTextFieldNome.setText("");
               //jTextFieldLotacao.setText("");
               jTextAreaAtividades.setText("");
               jTextFieldQtd.setText("");
               jTextFieldValor.setText("");
               jTextFieldNome.requestFocus();
       }
   }
